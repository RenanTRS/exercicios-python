#Entrada de dados
n1 = int(input("Digite um número:\n")) #variável n1 do tipo int -> inteiro
n2 = int(input("Digite outro número:\n")) #variável n2 do tipo int -> inteiro

s = n1 + n2 #variável s recebe a soma das variáveis n1 e n2

#Saída de dados
print("A soma entre {} e {} vale: {}".format(n1, n2, s)) #Saída de dados imprimindo as variável n1, n2 e s

